int		ft_sqrt(int nb)
{
	int h;

	h = 1;
	if (nb > 0)
	{
		while (h * h != nb)
		{
			h++;
			if (h * h > nb)
				return (0);
		}
	}
	else
		return (0);
	return (h);
}
