#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*ptr;

	i = 0;
	ptr = s;
	while (n)
	{
		ptr[i] = '\0';
		n--;
		i++;
	}
}
