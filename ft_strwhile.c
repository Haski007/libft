char	*ft_strwhile(const char *s, int c)
{
	int		i;
	char	*str;

	str = (char *)s;
	i = 0;
	while (str[i] != c)
		i++;
	str[i] = '\0';
	while (str[++i])
		str[i] = '\0';
	return (str);
}
